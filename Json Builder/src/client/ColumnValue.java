package client;

public class ColumnValue {

	protected String codeColumnValue = "";
	protected String decodeColumnValue = "";
	protected String populatedFieldValue = "";
	
	public String getCodeColumnValue() {
		return codeColumnValue;
	}
	public void setCodeColumnValue(String codeColumnValue) {
		this.codeColumnValue = codeColumnValue;
	}
	public String getDecodeColumnValue() {
		return decodeColumnValue;
	}
	public void setDecodeColumnValue(String decodeColumnValue) {
		this.decodeColumnValue = decodeColumnValue;
	}
	public String getPopulatedFieldValue() {
		return populatedFieldValue;
	}
	public void setPopulatedFieldValue(String populatedFieldValue) {
		this.populatedFieldValue = populatedFieldValue;
	}
	
}
